numero1 = int(input(" Numero 1:  "))  #  iput devuelve siempre un tipo de dato string, con la funcion casting decidimos que tipo de dato se requiere
numero2 = 10  #  Tipo de dato int  
suma = int(numero1) + numero2  #  Tambien se puede hacer el casting cuando utilicemos el dato 
resta = numero1-numero2  #  se define la variable resta que hace la resta de numero1 y numero 2
multiplicacion = numero1*numero2  #  se define la variable multiplicación que hace la multiplcación de numero1 y numero 2
division = numero1/numero2  #  se define la variable divición que hace la divición de numero1 y numero 2
potencia = numero1**numero2  #  se define la variable potencia que hace la potencia de numero1 elevado a numero2
print ("suma =",suma)  # imprime la variable suma
print ("resta =",resta)  #  imprime la variable resta
print ("Multiplicación =",multiplicacion)  #  imprime la variable multiplicación 
print ("División =",division)  #  imprime la variable divición 
print ("Potencia =",potencia)  #  imprime la variable potencia